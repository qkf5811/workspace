package com.dkt.school.sch.model;

public class Grade {
    private Integer id;

    private String name;

    private Integer eduStageType;

    private Integer seq;

    private Integer isGraduation;

    private String remark;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getEduStageType() {
        return eduStageType;
    }

    public void setEduStageType(Integer eduStageType) {
        this.eduStageType = eduStageType;
    }

    public Integer getSeq() {
        return seq;
    }

    public void setSeq(Integer seq) {
        this.seq = seq;
    }

    public Integer getIsGraduation() {
        return isGraduation;
    }

    public void setIsGraduation(Integer isGraduation) {
        this.isGraduation = isGraduation;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}