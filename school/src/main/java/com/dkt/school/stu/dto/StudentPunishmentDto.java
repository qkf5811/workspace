package com.dkt.school.stu.dto;

import com.dkt.school.stu.model.StudentPunishment;

public class StudentPunishmentDto extends StudentPunishment{

	//学号
	private String code;
	//班别
	private Integer classId;
	//姓名
	private String name;
	//惩罚类型
	private String punishmentTypeStr;
	//所在班级
	private String className;

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public Integer getClassId() {
		return classId;
	}

	public void setClassId(Integer classId) {
		this.classId = classId;
	}
	
	public String getPunishmentTypeStr() {
		return punishmentTypeStr;
	}

	public void setPunishmentTypeStr(String punishmentTypeStr) {
		this.punishmentTypeStr = punishmentTypeStr;
	}

	@Override
	public String toString() {
		return "StudentPunishmentDto [code=" + code + ", classId=" + classId
				+ ", name=" + name + ", punishmentTypeStr=" + punishmentTypeStr
				+ ", className=" + className + ", getStudentId()="
				+ getStudentId() + ", getPunishmentType()="
				+ getPunishmentType() + ", getContent()=" + getContent()
				+ ", getRemark()=" + getRemark() + "]";
	}

	

}
