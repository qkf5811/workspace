package com.dkt.school.edu.model;

import java.util.Date;

public class CourseScore {
    private Integer id;

    private Integer teachCourseId;

    private Integer studentId;

    private Integer examNatureType;

    private Float hundredUsualScore;

    private Float hundredExamScore;

    private Float hundredTotalScore;

    private Integer gradeUsualScore;

    private Integer gradeExamScore;

    private Integer gradeTotalScore;

    private Date inputTime;
    
   

	public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getTeachCourseId() {
        return teachCourseId;
    }

    public void setTeachCourseId(Integer teachCourseId) {
        this.teachCourseId = teachCourseId;
    }

    public Integer getStudentId() {
        return studentId;
    }

    public void setStudentId(Integer studentId) {
        this.studentId = studentId;
    }

    public Integer getExamNatureType() {
        return examNatureType;
    }

    public void setExamNatureType(Integer examNatureType) {
        this.examNatureType = examNatureType;
    }

    public Float getHundredUsualScore() {
        return hundredUsualScore;
    }

    public void setHundredUsualScore(Float hundredUsualScore) {
        this.hundredUsualScore = hundredUsualScore;
    }

    public Float getHundredExamScore() {
        return hundredExamScore;
    }

    public void setHundredExamScore(Float hundredExamScore) {
        this.hundredExamScore = hundredExamScore;
    }

    public Float getHundredTotalScore() {
        return hundredTotalScore;
    }

    public void setHundredTotalScore(Float hundredTotalScore) {
        this.hundredTotalScore = hundredTotalScore;
    }

    public Integer getGradeUsualScore() {
        return gradeUsualScore;
    }

    public void setGradeUsualScore(Integer gradeUsualScore) {
        this.gradeUsualScore = gradeUsualScore;
    }

    public Integer getGradeExamScore() {
        return gradeExamScore;
    }

    public void setGradeExamScore(Integer gradeExamScore) {
        this.gradeExamScore = gradeExamScore;
    }

    public Integer getGradeTotalScore() {
        return gradeTotalScore;
    }

    public void setGradeTotalScore(Integer gradeTotalScore) {
        this.gradeTotalScore = gradeTotalScore;
    }

    public Date getInputTime() {
        return inputTime;
    }

    public void setInputTime(Date inputTime) {
        this.inputTime = inputTime;
    }
}