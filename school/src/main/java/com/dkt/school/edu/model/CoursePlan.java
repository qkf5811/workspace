package com.dkt.school.edu.model;

public class CoursePlan {
    private Integer id;

    private Integer majorId;

    private Integer fieldId;

    private Integer gradeId;

    private Integer termType;

    private Integer courseId;

    private Integer courseType;

    private Integer examineType;

    private Float credit;

    private Integer hours;

    private String remark;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMajorId() {
        return majorId;
    }

    public void setMajorId(Integer majorId) {
        this.majorId = majorId;
    }

    public Integer getFieldId() {
        return fieldId;
    }

    public void setFieldId(Integer fieldId) {
        this.fieldId = fieldId;
    }

    public Integer getGradeId() {
        return gradeId;
    }

    public void setGradeId(Integer gradeId) {
        this.gradeId = gradeId;
    }

    public Integer getTermType() {
        return termType;
    }

    public void setTermType(Integer termType) {
        this.termType = termType;
    }

    public Integer getCourseId() {
        return courseId;
    }

    public void setCourseId(Integer courseId) {
        this.courseId = courseId;
    }

    public Integer getCourseType() {
        return courseType;
    }

    public void setCourseType(Integer courseType) {
        this.courseType = courseType;
    }

    public Integer getExamineType() {
        return examineType;
    }

    public void setExamineType(Integer examineType) {
        this.examineType = examineType;
    }

    public Float getCredit() {
        return credit;
    }

    public void setCredit(Float credit) {
        this.credit = credit;
    }

    public Integer getHours() {
        return hours;
    }

    public void setHours(Integer hours) {
        this.hours = hours;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

	@Override
	public String toString() {
		return "CoursePlan [id=" + id + ", majorId=" + majorId + ", fieldId="
				+ fieldId + ", gradeId=" + gradeId + ", termType=" + termType
				+ ", courseId=" + courseId + ", courseType=" + courseType
				+ ", examineType=" + examineType + ", credit=" + credit
				+ ", hours=" + hours + ", remark=" + remark + "]";
	}
    
}