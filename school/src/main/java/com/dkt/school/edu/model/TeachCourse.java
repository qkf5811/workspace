package com.dkt.school.edu.model;

import java.util.Date;

public class TeachCourse {
    private Integer id;

    private Integer studyYearId;

    private Integer termType;

    private Integer courseId;

    private Integer coursePlanId;

    private Integer courseSeq;

    private String classes;

    private Date examDate;

    private Integer scoreType;

    private Float usualScoreRatio;

    private Date inputTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getStudyYearId() {
        return studyYearId;
    }

    public void setStudyYearId(Integer studyYearId) {
        this.studyYearId = studyYearId;
    }

    public Integer getTermType() {
        return termType;
    }

    public void setTermType(Integer termType) {
        this.termType = termType;
    }

    public Integer getCourseId() {
        return courseId;
    }

    public void setCourseId(Integer courseId) {
        this.courseId = courseId;
    }

    public Integer getCoursePlanId() {
        return coursePlanId;
    }

    public void setCoursePlanId(Integer coursePlanId) {
        this.coursePlanId = coursePlanId;
    }

    public Integer getCourseSeq() {
        return courseSeq;
    }

    public void setCourseSeq(Integer courseSeq) {
        this.courseSeq = courseSeq;
    }

    public String getClasses() {
        return classes;
    }

    public void setClasses(String classes) {
        this.classes = classes;
    }

    public Date getExamDate() {
        return examDate;
    }

    public void setExamDate(Date examDate) {
        this.examDate = examDate;
    }

    public Integer getScoreType() {
        return scoreType;
    }

    public void setScoreType(Integer scoreType) {
        this.scoreType = scoreType;
    }

    public Float getUsualScoreRatio() {
        return usualScoreRatio;
    }

    public void setUsualScoreRatio(Float usualScoreRatio) {
        this.usualScoreRatio = usualScoreRatio;
    }

    public Date getInputTime() {
        return inputTime;
    }

    public void setInputTime(Date inputTime) {
        this.inputTime = inputTime;
    }

	@Override
	public String toString() {
		return "TeachCourse [id=" + id + ", studyYearId=" + studyYearId
				+ ", termType=" + termType + ", courseId=" + courseId
				+ ", coursePlanId=" + coursePlanId + ", courseSeq=" + courseSeq
				+ ", classes=" + classes + ", examDate=" + examDate
				+ ", scoreType=" + scoreType + ", usualScoreRatio="
				+ usualScoreRatio + ", inputTime=" + inputTime + "]";
	}
    
    
}