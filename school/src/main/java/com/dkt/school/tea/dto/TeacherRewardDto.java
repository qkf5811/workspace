package com.dkt.school.tea.dto;

import com.dkt.school.tea.model.TeacherReward;

public class TeacherRewardDto extends TeacherReward {

	//学年
	private String schoolYear;
	//教师编号
	private String code;
	//教师姓名
	private String name;
	//担任职务
	private String nowPosition;
	//奖励日期
	private String rewardDateStr;
	//学期
	private String schoolTermTypeStr;
	//奖励类型
	private String rewardTypeStr;
	
	
	public String getSchoolYear() {
		return schoolYear;
	}
	public void setSchoolYear(String schoolYear) {
		this.schoolYear = schoolYear;
	}
	public String getSchoolTermTypeStr() {
		return schoolTermTypeStr;
	}
	public void setSchoolTermTypeStr(String schoolTermTypeStr) {
		this.schoolTermTypeStr = schoolTermTypeStr;
	}
	public String getRewardDateStr() {
		return rewardDateStr;
	}
	public void setRewardDateStr(String rewardDateStr) {
		this.rewardDateStr = rewardDateStr;
	}
	public String getNowPosition() {
		return nowPosition;
	}
	public void setNowPosition(String nowPosition) {
		this.nowPosition = nowPosition;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRewardTypeStr() {
		return rewardTypeStr;
	}
	public void setRewardTypeStr(String rewardTypeStr) {
		this.rewardTypeStr = rewardTypeStr;
	}
	@Override
	public String toString() {
		return "TeacherRewardDto [schoolYear"+ schoolYear +",code=" + code + ", name=" + name
				+ ", nowPosition=" + nowPosition + ", rewardDateStr="
				+ rewardDateStr + ", schoolTermTypeStr=" + schoolTermTypeStr
				+ ", rewardTypeStr=" + rewardTypeStr + "]";
	}
	
}
