package com.dkt.school.apv.dto;

import com.dkt.school.apv.model.Application;


public class ApplicationDto extends Application{
	private String mobile;
	private String phone;
	private String appTimeStr;
	private String userName;
	private String applicationStatusStr;
	private String applicationTypeStr;
	private String departmentName;
	private String nodeName;//审批节点
	
	public String getNodeName() {
		return nodeName;
	}
	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAppTimeStr() {
		return appTimeStr;
	}
	public void setAppTimeStr(String appTimeStr) {
		this.appTimeStr = appTimeStr;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getApplicationStatusStr() {
		return applicationStatusStr;
	}
	public void setApplicationStatusStr(String applicationStatusStr) {
		this.applicationStatusStr = applicationStatusStr;
	}
	public String getApplicationTypeStr() {
		return applicationTypeStr;
	}
	public void setApplicationTypeStr(String applicationTypeStr) {
		this.applicationTypeStr = applicationTypeStr;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	
	
	
	
}
